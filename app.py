# --- 3. ‡∏ü‡∏±‡∏á‡∏Å‡πå‡∏ä‡∏±‡∏ô‡πÄ‡∏ï‡∏£‡∏µ‡∏¢‡∏°‡∏£‡∏π‡∏õ‡∏Å‡πà‡∏≠‡∏ô‡∏™‡πà‡∏á‡πÉ‡∏´‡πâ AI ---

def import_and_predict(image_data, model):

    # EfficientNetB4 ‡πÉ‡∏ä‡πâ‡∏Ç‡∏ô‡∏≤‡∏î 300x300

    size = (300, 300)

   

    image = ImageOps.fit(image_data, size, Image.Resampling.LANCZOS)

    img_array = np.asarray(image)

   

    # EfficientNet ‡πÑ‡∏°‡πà‡∏ï‡πâ‡∏≠‡∏á‡∏´‡∏≤‡∏£ 255 (‡πÉ‡∏ä‡πâ‡∏Ñ‡πà‡∏≤ 0-255 ‡πÑ‡∏î‡πâ‡πÄ‡∏•‡∏¢)

    # ‡πÅ‡∏ï‡πà‡∏ï‡πâ‡∏≠‡∏á‡πÅ‡∏õ‡∏•‡∏á‡πÄ‡∏õ‡πá‡∏ô float32

    img_array = img_array.astype(np.float32)

   

    # ‡∏™‡∏£‡πâ‡∏≤‡∏á array 4 ‡∏°‡∏¥‡∏ï‡∏¥ (1, 300, 300, 3)

    data = np.ndarray(shape=(1, 300, 300, 3), dtype=np.float32)

    data[0] = img_array

   

    prediction = model.predict(data)

    return prediction

   

# --- 4. ‡∏™‡πà‡∏ß‡∏ô‡πÅ‡∏™‡∏î‡∏á‡∏ú‡∏•‡∏´‡∏ô‡πâ‡∏≤‡πÄ‡∏ß‡πá‡∏ö ---

